    <h2>About Me</h2>
    <p>

      Greetings, my name is Reza. I hail from Parepare City and was raised in Makassar, a rapidly developing
      metropolitan area in Indonesia. I have a fondness for travel, indulging in the scenic beauty of tourism within my
      country. These experiences have significantly shaped my perspective on life and profoundly influenced my
      professional approach.
      <br><br>
      As a graduate with a Master's degree in Guidance and Counseling, specializing in Special Education, I have
      acquired and refined the skills necessary to be an effective educator. Through my thesis and dissertation,
      utilizing Research and Development (RnD) methodologies, I successfully devised technological media aimed at
      enhancing the learning capabilities of children with disabilities. This accomplishment is one I take pride in, and
      I aspire to continue contributing to the advancement of this field.
      <br><br>

      My professional journey encompasses various roles, including serving as a Learning Management System (LMS) class
      administrator in the UNM Teacher Education Program, being a member of the Supervision Team at UP UKMPPG Kemdikbud,
      and contributing as a member of the Monitoring Team for PPG Prajabatan Kemdikbud for a period of three years.
      Furthermore, my experiences in organizing and participating in volunteer activities have equipped me with the
      ability to thrive under pressure and collaborate effectively within a team. I am eager to apply these acquired
      skills and knowledge to a new and challenging role, confident that my background will bring meaningful
      contributions to the workplace.
      <br><br>

      Beyond my professional pursuits, I harbor a keen interest in immersing myself in the realm of technology. I have
      diligently honed my skills in technology, web programming, and web security. The challenges and continuous
      learning opportunities presented by this dynamic field are aspects that I relish. I am enthusiastic about applying
      these skills and knowledge in a fresh and challenging role, fully believing in the potential to make a meaningful
      impact in my next professional endeavor.
    </p>
